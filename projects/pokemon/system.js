function enter() {
    const size = parseFloat(document.getElementById("size").value);
    const weight = parseFloat(document.getElementById("weight").value);
    const size_t = parseFloat(document.getElementById("size_t").value);
    const weight_t = parseFloat(document.getElementById("weight_t").value);

    console.log(size_t);
    console.log(weight_t);

    let i;
    let memory;
    let memory2;

    let results_size = new Array;
    let results_weight = new Array;
    let results_all = new Array;

    for (i of pokemon_details) {
        // サイズ
        memory = parseFloat(i[1]);
        if (size_t == 0) {
            if (size == memory) {
                results_size.push(`名前: ${i[0]}<br>\n　→ サイズ: ${i[1]}m<br>\n　→ 体重: ${i[2]}`);
            }
        } else {
            if (size - size_t <= memory && memory <= size + size_t) {
                results_size.push(`名前: ${i[0]}<br>\n　→ サイズ: ${i[1]}m<br>\n　→ 体重: ${i[2]}`);
            }
        }
        
        // 体重
        memory = parseFloat(i[2]);
        if (weight_t == 0) {
            if (weight == memory) {
                results_weight.push(`名前: ${i[0]}<br>\n　→ サイズ: ${i[1]}m<br>\n　→ 体重: ${i[2]}`);
            }
        } else {
            if (weight - weight_t <= memory && memory <= weight + weight_t) {
                results_weight.push(`名前: ${i[0]}<br>\n　→ サイズ: ${i[1]}m<br>\n　→ 体重: ${i[2]}`);
            }
        }
        
        
        // 両方
        memory = parseFloat(i[1]);
        memory2 = parseFloat(i[2]);
        if ( (size - size_t <= memory && memory <= size + size_t)
                && (weight - weight_t <= memory2 && memory2 <= weight + weight_t)) {
                    results_all.push(`名前: ${i[0]}<br>\n　→ サイズ: ${i[1]}m<br>\n　→ 体重: ${i[2]}`);
        }
    }

    results_size = results_size.join("<br><br>");
    results_weight = results_weight.join("<br><br>");
    results_all = results_all.join("<br><br>");

    const weight_r = document.getElementById("weight_r");
    const size_r = document.getElementById("size_r");
    const all_r = document.getElementById("all_r");
    
    weight_r.innerHTML = results_weight;
    size_r.innerHTML = results_size;
    all_r.innerHTML = results_all;
}
